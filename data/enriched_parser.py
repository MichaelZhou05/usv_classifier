        stats = get_enriched_feature_stats(data)
        print(f"  Total calls: {stats['total_calls']}")
        for feat, feat_stats in stats['per_feature'].items():
            print(f"  {feat}: mean={feat_stats['mean']:.3f}, std={feat_stats['std']:.3f}")
